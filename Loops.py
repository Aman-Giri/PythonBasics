# If else Conditions
greeting = "Good Morning"

if greeting == "Morning":
    print("Condition Matched")
else:
    print("Condition not matched")

print("If Else block is completed ")

# Loops
list = [1, 3, 4, 5, 6]
for i in list:
    print(i*2)

# printing the sum of the first 5 numbers
summation = 0
for j in range(1, 6):
    summation = summation + j
print("{}{}".format("The sum of first five number is: ", summation))

